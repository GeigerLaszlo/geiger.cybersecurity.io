// Function to change language
function changeLanguage(lang) {
    // Set the selected language in local storage
    localStorage.setItem('language', lang);

    // Get the translation object for the selected language
    const texts = translations[lang];

    // Update the content for each section
    document.querySelectorAll('.section').forEach((section, index) => {
        if (index === 0) {
            section.querySelector('h1').textContent = texts.project1.title;
            section.querySelectorAll('p')[0].textContent = texts.project1.text1;
            section.querySelectorAll('p')[1].textContent = texts.project1.text2;
        } else if (index === 1) {
            section.querySelector('h1').textContent = texts.project2.title;
            section.querySelector('p').textContent = texts.project2.text;
        } else if (index === 2) {
            section.querySelector('h1').textContent = texts.project3.title;
            section.querySelectorAll('p')[0].textContent = texts.project3.text1;
            section.querySelectorAll('p')[1].textContent = texts.project3.text2;
        } else if (index === 3) {
            section.querySelector('h1').textContent = texts.project4.title;
            section.querySelector('p').textContent = texts.project4.text;
        } else if (index === 4) {
            section.querySelector('h1').textContent = texts.project5.title;
            section.querySelector('p').textContent = texts.project5.text;
        }
    });
}

// Check if a language is already selected and set it on page load
document.addEventListener('DOMContentLoaded', () => {
    const storedLanguage = localStorage.getItem('language') || 'en';
    changeLanguage(storedLanguage);
});
